<?php 

function accum ($s) {

    $result = [];

    for ( $i = 0; $i < strlen( $s ); $i++ ) {
        $result[] = ucfirst( str_repeat( $s[$i], $i+1 ) );
    }

    return join( '-', $result);
}

var_dump(accum("ZpglnRxqenU"), "Z-Pp-Ggg-Llll-Nnnnn-Rrrrrr-Xxxxxxx-Qqqqqqqq-Eeeeeeeee-Nnnnnnnnnn-Uuuuuuuuuuu");
